#include <stdio.h>

int main(void) {
    
    int max = 0;
    printf("Hey gimme yo max: ");
    scanf("%d", &max);
    
    int min = 0;
    printf("Hey gimme yo min: ");
    scanf("%d", &min);
    
    int i = min;
    while (i <= max) {
        printf("%d\n", i);
        i++;
    }
    
    return 0;
}
